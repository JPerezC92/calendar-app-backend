import { model, Schema } from "mongoose";

import { User } from "../types";

const UserMongoSchema = new Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
});

export const UserModel = model<User>("User", UserMongoSchema);
